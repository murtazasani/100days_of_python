# To Find Cube of Number

number = int(input("Enter a Number: "))
square = number * number
cube = number ** 3
p4 = number ** 2 ** 2
p5 = number ** 2 ** 3

print("Square of", number, "=", square)
print("Cube of", number, "=", cube)
print("Power 4 of", number, "=", p4)
print("Power 5 of", number, "=", p5)
