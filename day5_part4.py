# Some Functions on Dictionary

dob = {"Date": 11, "Month": 2, "Year": 1998}

print(dob)
print(dob.keys())
print(dob.values())
print(dob["Date"])

groclis = {"Fruits": ["Apple", "Mango", "Banana"],
           "Vegetables": ["Tomato", "Potato", "Carrot"]}
print(groclis)
print(groclis.keys())
print(groclis.values())
print(groclis["Fruits"])
print(groclis["Vegetables"][1])
